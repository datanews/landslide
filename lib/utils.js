// Parse boolean from a string (text message)
function parseBooleanFromString(input) {
  try {
    input = ('' + input).toLowerCase();
    return (input === 'true' || input === 'yes' || input === 'y' || input === 'si' || input === 's√≠' || input === true);
  }
  catch(e) {
    return false;
  }
}

